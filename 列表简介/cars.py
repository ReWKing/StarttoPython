cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)

# 使用方法sort（）
'''
cars.sort()
print(cars)
'''
# 反向排序
'''
cars.sort(reverse=True)
print(cars)
'''
# 使用函数sorted()对列表进行临时排序
'''
print("Here is origin list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the origin list again:")
print(cars)
'''
# 倒着打印列表
'''
cars.reverse()
print(cars)
'''
